-- Helper function to get audio event by category
function audioLibrary.getAudioEvent(category, subcategory, item)
    local cat = audioLibrary[category]
    if not cat then
        return nil
    end
    
    if subcategory and cat[subcategory] then
        local sub = cat[subcategory]
        if item and sub[item] then
            return sub[item]
        end
        return nil
    end
    
    if item and cat[item] then
        return cat[item]
    end
    
    return nil
end

-- Helper to get event path by GUID
function audioLibrary.getPathByGuid(guid)
    for category, items in pairs(audioLibrary) do
        if type(items) == "table" and category ~= "getAudioEvent" and category ~= "getPathByGuid" then
            for name, data in pairs(items) do
                if data.guid == guid then
                    return data.path
                end
            end
        end
    end
    return nil
end

return audioLibrary
