    static float Fade(float t) => t * t * t * (t * (t * 6 - 15) + 10);
    static float Lerp(float t, float a, float b) => a + t * (b - a);
    static float Grad(int hash, float x, float y)
    {
        int h = hash & 15;
        float u = h < 8 ? x : y;
        float v = h < 8 ? y : x;
        return ((h & 1) == 0 ? u : -u) + ((h & 2) == 0 ? v : -v);
    }

    public static float Noise(float x, float y, int seed = 0)
    {
        int xi = (int)Math.Floor(x) & 255;
        int yi = (int)Math.Floor(y) & 255;

        float xf = x - (float)Math.Floor(x);
        float yf = y - (float)Math.Floor(y);

        float u = Fade(xf);
        float v = Fade(yf);

        int p00 = perm[(perm[xi] + yi + seed) & 255];
        int p10 = perm[(perm[(xi + 1) & 255] + yi + seed) & 255];
        int p01 = perm[(perm[xi] + (yi + 1) & 255 + seed) & 255];
        int p11 = perm[(perm[(xi + 1) & 255] + (yi + 1) & 255 + seed) & 255];

        float g00 = Grad(p00, xf, yf);
        float g10 = Grad(p10, xf - 1, yf);
        float g01 = Grad(p01, xf, yf - 1);
        float g11 = Grad(p11, xf - 1, yf - 1);

        float nx0 = Lerp(u, g00, g10);
        float nx1 = Lerp(u, g01, g11);
        return Lerp(v, nx0, nx1);
    }
